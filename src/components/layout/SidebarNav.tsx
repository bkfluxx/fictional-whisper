function NavLink({
  href,
  label,
  icon,
  isActive,
}: {
  href: string;
  label: string;
  icon: React.ReactNode;
  isActive: boolean;
}) {
  return (
    <Link
      href={href}
      className={`flex items-center gap-2.5 px-3 py-2 rounded-lg text-sm transition-colors ${
        isActive
          ? "bg-neutral-800 text-white font-medium"
          : "text-neutral-400 hover:bg-neutral-800/60 hover:text-neutral-200"
      }`}
    >
      {icon}
      {label}
    </Link>
  );
}

export default function SidebarNav() {
  const pathname = usePathname();

  return (
    <nav className="flex flex-col gap-0.5 flex-1 min-h-0 overflow-y-auto">
      {MAIN_NAV.map((item) => {
        const isActive =
          item.href === "/journal"
            ? pathname === "/journal" ||
              (pathname.startsWith("/journal/") &&
                !pathname.startsWith("/journal/new"))
            : pathname.startsWith(item.href);
        return <NavLink key={item.href} {...item} isActive={isActive} />;
      })}

      {/* Settings — pinned to bottom */}
      <div className="mt-auto pt-3 border-t border-neutral-800/60">
        {BOTTOM_NAV.map((item) => (
          <NavLink
            key={item.href}
            {...item}
            isActive={pathname.startsWith(item.href)}
          />
        ))}
      </div>
    </nav>
  );
}
